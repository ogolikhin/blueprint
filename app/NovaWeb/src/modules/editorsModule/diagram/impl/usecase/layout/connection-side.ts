/**
 * Defines the side of a node where a connections starts or ends.
 */
export enum ConnectionSide {
    /**
     * The connection starts or ends at the top side of a node.
     */
    Top,
        /**
         * The connection starts or ends at the left side of a node.
         */
    Left,
        /**
         * The connection starts or ends at the right side of a node.
         */
    Right,
        /**
         * The connection starts or ends at the bottom side of a node.
         */
    Bottom
}
